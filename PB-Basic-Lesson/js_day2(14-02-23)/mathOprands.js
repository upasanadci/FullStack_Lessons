//Math.ceil() will rounded up to the nearest number

console.log(Math.ceil(3.7));//4
console.log(Math.ceil(-3.7));//-3
console.log(Math.ceil(4.2));//5

//Math.floor() will rounded down to the nearest number

let a = 4;
let b =3;
let c = a/b;
console.log("result is",Math.round(c));

console.log(Math.floor(3.7))//3
console.log(Math.floor(4.2));//4
console.log(Math.floor(-3.8));//-4

//Math.round() will give nearest integer

console.log(Math.round(3.7))//4
console.log(Math.round(3.2));//3
console.log(Math.round(3.5));//4

