// == check only the value not the type
// === check both value and type
//== opposite of !=
//=== opposite of !==
const orange = "5";
const mango = 5;
console.log(orange == mango);//true
console.log(orange === mango);//false
console.log(orange != mango);// false
console.log(orange !== mango);// true

